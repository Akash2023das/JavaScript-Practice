//Assignment Operators

let a=10;
let b=3;
a+=b;//a=a+b
console.log("a+=b = "+a);
a-=b;//a=a-b
console.log("a-=b = "+a);
a*=b;//a=a*b
console.log("a*=b = "+a);
a/=b;//a=a/b
console.log("a/=b = "+a);
a%=b;//a=a%b
console.log("a%=b = "+a);
a**=b;//a=a**b
console.log("a**=b = "+a);
//Output:
//a+=b = 13
//a-=b = 10
//a*=b = 30
//a/=b = 10
//a%=b = 1
//a**=b = 1
