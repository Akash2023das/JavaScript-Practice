//Arithmetic operators
let a=10;
let b=3;
console.log("a+b = "+(a+b));
console.log("a-b = "+(a-b));
console.log("a*b = "+(a*b));
console.log("a/b = "+(a/b));
console.log("a%b = "+(a%b));
console.log("a**b = "+(a**b));

//Increment and decrement operators
let c=10;
console.log("c++ = "+(c++));//10
console.log("c = "+c);//11
console.log("++c = "+(++c));//12
console.log("c = "+c);//12
console.log("c-- = "+(c--));//12
console.log("c = "+c);//11
console.log("--c = "+(--c));//10
console.log("c = "+c);//10

