//Comparison Operators

let a=10;
let b=3;
console.log("a>b = "+(a>b));
console.log("a<b = "+(a<b));
console.log("a>=b = "+(a>=b));
console.log("a<=b = "+(a<=b));
console.log("a==b = "+(a==b));
console.log("a!=b = "+(a!=b));
console.log("a===b = "+(a===b));
console.log("a!==b = "+(a!==b));
//Output:
//a>b = true
//a<b = false
//a>=b = true
//a<=b = false
//a==b = false
//a!=b = true
//a===b = false
//a!==b = true
