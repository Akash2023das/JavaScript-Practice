//for loop example
for (let i = 0; i < 10; i++) {
    console.log(i);
}
console.log("---------------");



//while loop example
let i = 0;
while (i < 10) {
    console.log(i);
    i++;
}
console.log("---------------");



//do while loop example
let b = 0;
do {
    console.log(i);
    b++;
} while (b < 10);

console.log("---------------");




//for of loop example
let arr = [1, 2, 3, 4, 5];
for (let i of arr) {
    console.log(i);
}
console.log("---------------");



